
// Print first 10 netural number;  

public class DoWhileLoopInJavaP1{
    public static void main(String[] args){
        System.out.println("Do While loop in java!! Example 01 ");
        System.out.println("First 10 netural number is :- ");
        int i = 1;
        do { 
            System.out.println(i);
            i++;
            
        } while (i <= 10);

    }
}